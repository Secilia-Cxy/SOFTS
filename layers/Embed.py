class ShareConvEmbedding(nn.Module):
    def __init__(self, d_model, patch_len, stride, padding, dropout):
        super(ShareConvEmbedding, self).__init__()
        # Patching
        self.patch_len = patch_len
        self.stride = stride
        self.padding_patch_layer = nn.ReplicationPad1d(padding // 2)

        # Backbone, Input encoding: projection of feature vectors onto a d-dim vector space
        self.value_embedding = nn.Linear(patch_len, 1, bias=False)

        # Positional embedding
        self.position_embedding = PositionalEmbedding(d_model)

        # Residual dropout
        self.dropout = nn.Dropout(dropout)

    def forward(self, x):  # [B,C,T]
        # do patching
        n_vars = x.shape[1]
        x = self.padding_patch_layer(x)
        x = x.unfold(dimension=-1, size=self.patch_len, step=self.stride)
        x = torch.reshape(x, (x.shape[0] * x.shape[1], x.shape[2], x.shape[3]))
        # Input encoding
        x = self.value_embedding(x).squeeze()
        x = torch.reshape(x, (-1, n_vars, x.shape[1])).permute(0, 2, 1)
        x = x + self.position_embedding(x)
        return self.dropout(x), n_vars


class DataEmbedding_inverted(nn.Module):
    def __init__(self, c_in, d_model, embed_type='fixed', freq='h', dropout=0.1):
        super(DataEmbedding_inverted, self).__init__()
        self.value_embedding = nn.Linear(c_in, d_model)
        self.dropout = nn.Dropout(p=dropout)

    def forward(self, x, x_mark):
        x = x.permute(0, 2, 1)
        # x: [Batch Variate Time]
        if x_mark is None:
            x = self.value_embedding(x)
        else:
            # the potential to take covariates (e.g. timestamps) as tokens
            x = self.value_embedding(torch.cat([x, x_mark.permute(0, 2, 1)], 1))
        # x: [Batch Variate d_model]
        return self.dropout(x)

class RNNEmbedding(nn.Module):
    def __init__(self, d_model, dropout=0.1, method='rnn'):
        super(RNNEmbedding, self).__init__()
        if method == 'rnn':
            self.rnn = nn.RNN(1, d_model, batch_first=True)
        elif method == 'lstm':
            self.rnn = nn.LSTM(1, d_model, batch_first=True)
        elif method == 'gru':
            self.rnn = nn.GRU(1, d_model, batch_first=True)
        self.dropout = nn.Dropout(p=dropout)

    def forward(self, x):
        # x: [B T C]
        n_vars = x.shape[-1]
        x = rearrange(x, 'b t c -> (b c) t 1')
        x, _ = self.rnn(x)
        return self.dropout(x), n_vars